// JavaScript isomórfico funciona no contexto de um aplicativo de página única (SPA). Em um SPA típico, a maior parte da lógica do aplicativo, incluindo roteamento, é encapsulado em um arquivo JavaScript empacotado que é enviado ao cliente. Embora isso libere o servidor, já que ele não precisa lidar com tantas solicitações, também torna o carregamento inicial lento para o cliente, já que todo o aplicativo precisa ser enviado ao cliente.
// Com o JavaScript isomórfico, quando uma página da web do cliente é solicitada pela primeira vez no servidor, a visualização da página é gerada no servidor, semelhante a páginas da web dinâmicas do lado do servidore enviado ao cliente. O cliente pode então renderizar a visualização imediatamente. Depois que a visualização inicial é renderizada, o SPA completo é baixado em segundo plano e as ações subsequentes são tratadas no lado do cliente.


